package org.tnsif.introduction;

public class CharDemo {

	public static void main(String[] args) {
		char ch='a';
		System.out.println(ch);
		
		// assigning number to character (A/c to ASCII table)
		char ch1 = 90;
		System.out.println(ch1);
		
		char ch2='\u00A7';
        System.out.println(ch2);
	}

}
